    public function get_batch()
    {
        $where = '';
        $where .= " WHERE is_deleted = 0";

        if (!empty($_REQUEST['search']['value'])) {
            $where .= " And ( name LIKE '%" . $_REQUEST['search']['value'] . "%' )";
        }

        $totalRecordsSql = "SELECT * FROM batch $where;";

        $res = $this->CommonModel->custome_query($totalRecordsSql);
        $totalRecords = count($res);
   
        $columns = array(
            0 => 'id',
            1 => 'id',
            2 => 'name',
            3 => 'status',
            4 => 'created',
            5 => 'action',
        );

        $sql = "SELECT *";
        $sql .= " FROM batch $where";
        $sql .= " ORDER BY " . $columns[$_REQUEST['order'][0]['column']] . "   " . $_REQUEST['order'][0]['dir'] . "  LIMIT " . $_REQUEST['start'] . " ," . $_REQUEST['length'] . "   ";

        $result = $this->CommonModel->custome_query($sql);

        $data_array = array();
        $count      = 0;
        
        foreach ($result as  $row) 
        {
            $data['no']         = ++$count;
            $data['id']         = $row->id;
            $data['name']       = $row->name;
            $data['status']       = $row->status;
            $data['created']    = $row->created;


            array_push($data_array, $data);
        }

        $json_data = array(
            "draw"            => intval($_REQUEST['draw']),
            "recordsTotal"    => intval($totalRecords),
            "recordsFiltered" => intval($totalRecords),
            "data"            => $data_array
        );

        return json_encode($json_data);
    }
    
    public function add_batch()
    {

        ////////////////////////Check Permission/////////////////
        $check = $this->permissionCheck('add_batch');
        $check_resp = json_decode($check);
        $status = $check_resp->status;
        if($status == 0)
        {
            $message = ['status' => '0', 'message' => 'Permission Deined!'];
            return json_encode($message);
            die;
        }
        ////////////////////////Check Permission/////////////////
        $year = date("Y"); 
        $months = array(
            'January',
            'February',
            'March',
            'April',
            'May',
            'June',
            'July ',
            'August',
            'September',
            'October',
            'November',
            'December',
        );


        $data_array = array();
        foreach($months as $row)
        {
            $name           =  $row.'-'.$year;
            $data['name']   = $name;
            $data['created']   = date('Y-m-d H:i:s');

            array_push($data_array,$data);
        }
        
        $check_exist = 'January-'.$year;
        $filter = array('name' => $check_exist);
        $exist = $this->CommonModel->get_single('batch',$filter);

        if(!empty($exist))
        {
            $message = ['status' => '0', 'message' => 'Batch Already Exist!'];
            return json_encode($message);
            die;
        }

        $insert = $this->db->table('batch')->insertBatch($data_array);
        
        if ($insert != false) 
        {

            $activity_title = "New Batch created named " . $name;
            $this->addActivityLog($activity_title);

            $message = ['status' => '1', 'message' => 'New Batch has been added!'];
            return json_encode($message);
        } 
        else 
        {
            $message = ['status' => '0', 'message' => 'Something went wrong!'];
            return json_encode($message);
        }
    }
    
